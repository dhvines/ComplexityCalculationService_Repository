
package com.ibm.migr.inventory.model;


public class Archives {

    private int ears;
    private int wars;
    private int rars;
    private int ejbs;
    private int utilityJars;
    private int appClients;

    public int getEars() {
        return ears;
    }

    public void setEars(int ears) {
        this.ears = ears;
    }

    public int getWars() {
        return wars;
    }

    public void setWars(int wars) {
        this.wars = wars;
    }

    public int getRars() {
        return rars;
    }

    public void setRars(int rars) {
        this.rars = rars;
    }

    public int getEjbs() {
        return ejbs;
    }

    public void setEjbs(int ejbs) {
        this.ejbs = ejbs;
    }

    public int getUtilityJars() {
        return utilityJars;
    }

    public void setUtilityJars(int utilityJars) {
        this.utilityJars = utilityJars;
    }

    public int getAppClients() {
        return appClients;
    }

    public void setAppClients(int appClients) {
        this.appClients = appClients;
    }

}
